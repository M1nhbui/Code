#include <bits/stdc++.h>
using namespace std;

int main() {
	long double a, b;
	cin >> a >> b;
	cout << setprecision(2) << fixed << a + b << "\n";
	cout << setprecision(2) << fixed << a - b << "\n";
	cout << setprecision(2) << fixed << a * b << "\n";
	cout << setprecision(2) << fixed << a / b;
}